package _01_NORMALES_;

public class _08_NEGATIVO_A_POSITIVO_ {

	public static void main(String[] args) {
		
		
		int a= -25;
		int b= 36;
		
		System.out.println(-a);				//AL SER -A PASA A SER POSITIVO
		b++;
		System.out.println(b);				//36 INCREMENTA EN 1
		b--;
		System.out.println(b);				//37 DECREMENTA EN 1

	}

}
