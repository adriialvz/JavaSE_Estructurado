package _03_BUCLES_;

public class _05_NUMEROS_DE_100_A_10_ {

	public static void main(String[] args) {
		
		
		for(int i=100;i>=10;i-=10) {
			System.out.print(i+"/");
		}
		
		
		
		System.out.println("\n");
		
		
		
		int c=100;
		
		while(c>=10) {
			System.out.println(c);
			c-=10;
		}

	}

}
